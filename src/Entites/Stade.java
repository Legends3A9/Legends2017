package Entites;

/**
 *
 * @author ali hamouda
 */
public class Stade {
    private String nom;

    public Stade(String nom) {
        this.nom = nom;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    @Override
    public String toString() {
        return "stade{" + "nom=" + nom + '}';
    }

    
    
    
}
